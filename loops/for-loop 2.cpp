#include <iostream>
using namespace std;

int main() {
    int size;

    cout << "Enter how many numbers you want to input: ";
    cin >> size;

    int num, evenCount = 0, oddCount = 0;

    for (int i = 1; i <= size; i++) {
        cout << "Enter number " << i << ": ";
        cin >> num;

        if (num % 2 == 0)
            evenCount++;
        else
            oddCount++;
    }

    cout << "\nTotal Even Numbers: " << evenCount << endl;
    cout << "Total Odd Numbers: " << oddCount << endl;

    return 0;
}
